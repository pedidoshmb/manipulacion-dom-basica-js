
// Ejemplo usar swich

var numero =1;

switch (numero) {
    case 1:
        console.log("Soy uno..!");
        break;
    case 2:
        console.log("Soy un 2..!")
        break;
    case 100:   
        console.log("Soy un 100..!")
        break;
    default:
        console.log("no valgo nada");
}